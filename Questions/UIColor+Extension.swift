import UIKit

extension UIColor {
	
	class var defaultBGcolor: UIColor {
		return UIColor(red: 0.92, green: 0.92, blue: 0.95, alpha: 1)
	}
	
	class var defaultSeparatorColor: UIColor {
		return UIColor(red: 0.783922, green: 0.780392, blue: 0.8, alpha: 1)
	}
	
	class var defaultTintColor: UIColor {
		return UIColor(red: 0, green: 0.478431, blue: 1, alpha: 1)
	}
}
